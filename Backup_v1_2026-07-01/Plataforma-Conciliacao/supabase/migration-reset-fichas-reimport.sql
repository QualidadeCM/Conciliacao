-- Atenção: a planilha tem 5 modelos que repetem entre códigos Sapiens
-- diferentes (CM-SCAM ↔ CAM0002 e CAM0006, CM-OTC0050L ↔ LAP0016 e LAP0034,
-- etc.). Portanto, modelo NÃO pode mais ser UNIQUE. A chave única real é
-- codigo_sapiens.
DROP INDEX IF EXISTS uniq_produtos_modelo_naonulo;
DROP INDEX IF EXISTS uniq_produtos_codigo_sapiens;
CREATE UNIQUE INDEX uniq_produtos_codigo_sapiens
  ON public.produtos (codigo_sapiens)
  WHERE codigo_sapiens IS NOT NULL AND codigo_sapiens <> '';

-- 1) DELETE das fichas placeholder em um único statement.
--    Preserva fichas com qualquer informação preenchida ou referenciadas
--    por análises.
DELETE FROM public.fichas_mestres
 WHERE id NOT IN (
   SELECT DISTINCT f.id
     FROM public.fichas_mestres f
     LEFT JOIN public.analises a ON a.ficha_id = f.id
    WHERE (f.nome_comercial IS NOT NULL AND f.nome_comercial <> '')
       OR (f.exemplo_nro_serie IS NOT NULL AND f.exemplo_nro_serie <> '')
       OR (f.convencao_nro_serie IS NOT NULL AND f.convencao_nro_serie <> '')
       OR (f.familia IS NOT NULL AND f.familia <> '')
       OR a.id IS NOT NULL
 );

