-- 1) Schema: modelo NULLable + UNIQUE parcial
ALTER TABLE public.produtos ALTER COLUMN modelo DROP NOT NULL;

DO $$
DECLARE c RECORD;
BEGIN
  FOR c IN
    SELECT conname FROM pg_constraint
     WHERE conrelid = 'public.produtos'::regclass
       AND contype = 'u'
  LOOP
    EXECUTE 'ALTER TABLE public.produtos DROP CONSTRAINT IF EXISTS ' || quote_ident(c.conname);
    RAISE NOTICE 'Removida UNIQUE de produtos: %', c.conname;
  END LOOP;
END $$;

DROP INDEX IF EXISTS uniq_produtos_modelo_naonulo;
CREATE UNIQUE INDEX uniq_produtos_modelo_naonulo
  ON public.produtos (modelo)
  WHERE modelo IS NOT NULL AND modelo <> '';

